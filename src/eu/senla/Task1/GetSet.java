package eu.senla.Task1;

public class GetSet {


        private byte a;
        private short c;
        private float g;
        private double h;
        private char j;
        private boolean k;


        public byte getA () {
            return a;
        }

        public void setA ( byte a){
            this.a = a;
        }

        public short getC () {
            return c;
        }

        public void setC ( short c){
            this.c = c;
        }

    public float getG() {
        return g;
    }

    public void setG(float g) {
        this.g = g;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public char getJ() {
        return j;
    }

    public void setJ(char j) {
        this.j = j;
    }

    public boolean isK() {
        return k;
    }

    public void setK(boolean k) {
        this.k = k;
    }
}
